$(document).ready(function(){
    $(".btn").mouseenter(function(){
      $(this).css({"background-color": "#000000"});
       $(this).css({"color": "#ffffff"});
    }).mouseleave(function(){
      $(this).css({"background-color": "white"});
      $(this).css({"color": "#000000"});
    });
 $(".button").mouseenter(function(){
      $(this).css({"background-color": "#000000"});
       $(this).css({"color": "#ffffff"});
    }).mouseleave(function(){
      $(this).css({"background-color": "white"});
      $(this).css({"color": "#000000"});
    });
});